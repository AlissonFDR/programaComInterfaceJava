    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        //cadastrar
        if((txtDesc.getText()).isEmpty()==false&&(txtQtd.getText()).isEmpty()==false&&(txtPrec.getText()).isEmpty()==false){
            boolean erro=false;
            Produtos p = new Produtos();
            p.setId(Integer.toString(idProduto));
            p.setDescricao(txtDesc.getText());
            try{
                p.setQuantidade(Integer.parseInt(txtQtd.getText()));
            }catch(NumberFormatException ex){
                JOptionPane.showMessageDialog(null, "Quantidade inválida!", "Erro", JOptionPane.ERROR_MESSAGE);
                erro=true;
            }
            try{
                p.setPreco(Double.parseDouble(txtPrec.getText()));
            }catch(NumberFormatException ex){
                JOptionPane.showMessageDialog(null, "Preço inválido!", "Erro", JOptionPane.ERROR_MESSAGE);
                erro=true;
            }
            if(erro==false){
                DefaultTableModel dtmProdutos = (DefaultTableModel) tabProdutos.getModel();
                Object[] dados = {txtDesc.getText(),txtQtd.getText(),txtPrec.getText(),idProduto};
                BancoDeDados.adicionar(p);
                idProduto++;
                dtmProdutos.addRow(dados);
                //add excluir
                DefaultTableModel dteProdutos = (DefaultTableModel) tabProdutos1.getModel();
                dteProdutos.addRow(dados);
                //add editar
                DefaultTableModel dteditProdutos = (DefaultTableModel) tabProdutosAlterar.getModel();
                dteditProdutos.addRow(dados);
                //add pesquisar
                DefaultTableModel dtPesquisarProdutos = (DefaultTableModel) tabProdutosPesquisar.getModel();
                dtPesquisarProdutos.addRow(dados);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

