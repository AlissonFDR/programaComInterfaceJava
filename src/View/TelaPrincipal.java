    private void butaoSalvarCadastroClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butaoSalvarCadastroClienteActionPerformed
        if((txtNomeCliente.getText()).isEmpty()==false && (txtCpfCliente.getText()).isEmpty()==false && (txtCidadeCliente.getText()).isEmpty()==false && (txtUfCliente.getText()).isEmpty()==false && (txtDataNascimentoCliente.getText()).isEmpty()==false && (txtEnderecoCliente.getText()).isEmpty()==false && (txtNumeroEnderecoCliente.getText()).isEmpty()==false && (txtTelefoneCliente.getText()).isEmpty()==false && !"<Selecione>".equals(txtSexoCliente.getSelectedItem().toString())){
            Cliente Ncliente = new Cliente();
            Ncliente.setId(idCliente);
            Ncliente.setNome(txtNomeCliente.getText());
            Ncliente.setCpf(txtCpfCliente.getText());
            Ncliente.setDataDeNascimento(txtDataNascimentoCliente.getText());
            Ncliente.setSexo(txtSexoCliente.getSelectedItem().toString());
            Ncliente.setCidade(txtCidadeCliente.getText());
            Ncliente.setUf(txtUfCliente.getText());
            Ncliente.setEndereco(txtEnderecoCliente.getText());
            Ncliente.setNumero(txtNumeroEnderecoCliente.getText());
            Ncliente.setTelefone(txtTelefoneCliente.getText());
            String clientes="";
            for(Cliente C : BancoDeDados.getClientela()){
                clientes += C + "\n"; 
            }
            if(!"000.000.000-00".equals(txtCpfCliente.getText()) && !"00/00/0000".equals(txtDataNascimentoCliente.getText())){
                BancoDeDados.adicionar(Ncliente);
                //Adiciona na Tabela pesquisa usuarios
                DefaultTableModel pesquisaClientes = (DefaultTableModel) tabPesquisaClientes.getModel();
                String protegeCpf = txtCpfCliente.getText();
                protegeCpf = protegeCpf.replace(protegeCpf.substring(3, 10), ".***.**");
                Object[] dadosClientes = {Integer.toString(idCliente),txtNomeCliente.getText(),protegeCpf,txtCidadeCliente.getText(),txtUfCliente.getText(),txtTelefoneCliente.getText()};
                pesquisaClientes.addRow(dadosClientes);
                //Adciona na Tabela excluir Usuarios
                DefaultTableModel excluirClientes = (DefaultTableModel) tabExcluirClientes.getModel();
                excluirClientes.addRow(dadosClientes);
                //Adiciona na Tabela Alterar Ususarios
                DefaultTableModel alterarClientes = (DefaultTableModel) tabClientesAlterar.getModel();
                alterarClientes.addRow(dadosClientes);
                JOptionPane.showMessageDialog(null, "Adicionado com sucesso!");
                idCliente++;
                txtNomeCliente.setText("");
                txtCpfCliente.setText("00000000000");
                txtDataNascimentoCliente.setText("00000000");
                txtSexoCliente.setSelectedIndex(0);
                txtCidadeCliente.setText("");
                txtUfCliente.setText("");
                txtEnderecoCliente.setText("");
                txtNumeroEnderecoCliente.setText("");
                txtTelefoneCliente.setText("");
            }else{
                JOptionPane.showMessageDialog(null, "CPF ou Data de nascimento inválidos!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!", "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_butaoSalvarCadastroClienteActionPerformed

