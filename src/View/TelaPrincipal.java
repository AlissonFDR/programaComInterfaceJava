    private void botaoAlterarUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAlterarUserActionPerformed
        if(tabUsuariosAlterar.getSelectedRow() != -1){ 
            if(comboBoxAlterarUser.getSelectedItem()=="Nome"){
                BancoDeDados.editarUsuarioNome(tabUsuariosAlterar.getValueAt(tabUsuariosAlterar.getSelectedRow(), 0).toString(), txtAlterarUser.getText());
                tabExcluirUsuario.setValueAt(txtAlterarUser.getText(), tabUsuariosAlterar.getSelectedRow(), 1);
                tabUsuariosPesquisa.setValueAt(txtAlterarUser.getText(),tabUsuariosAlterar.getSelectedRow(), 1);
                tabUsuariosAlterar.setValueAt(txtAlterarUser.getText(),tabUsuariosAlterar.getSelectedRow(), 1);
                txtAlterarUser.setText("");
            }
            if(comboBoxAlterarUser.getSelectedItem()=="CPF"){
                String testeCpf = txtAlterarUser.getText();
                if(testeCpf.length() == 11){
                    testeCpf = testeCpf.substring(0,3)+"."+
                               testeCpf.substring(3,6)+"."+
                               testeCpf.substring(6,9)+"-"+
                               testeCpf.substring(9,11);
                    String protegeCpf = testeCpf;
                    protegeCpf = protegeCpf.replace(protegeCpf.substring(3, 10), ".***.**");
                    BancoDeDados.editarUsuarioCpf(tabUsuariosAlterar.getValueAt(tabUsuariosAlterar.getSelectedRow(), 0).toString(), txtAlterarUser.getText());
                    tabExcluirUsuario.setValueAt(protegeCpf,tabUsuariosAlterar.getSelectedRow(), 2);
                    tabUsuariosPesquisa.setValueAt(protegeCpf,tabUsuariosAlterar.getSelectedRow(), 2);
                    tabUsuariosAlterar.setValueAt(protegeCpf,tabUsuariosAlterar.getSelectedRow(), 2);
                    txtAlterarUser.setText("");
                }else{
                    JOptionPane.showMessageDialog(null,"Número de CPF inválido! (11 digitos)");
                }
            }
            if(comboBoxAlterarUser.getSelectedItem()=="Usuário"){
                if(BancoDeDados.testaUsuario(txtAlterarUser.getText())==true){
                    BancoDeDados.editarUsuario(tabUsuariosAlterar.getValueAt(tabUsuariosAlterar.getSelectedRow(), 0).toString(),txtAlterarUser.getText());
                    tabExcluirUsuario.setValueAt(txtAlterarUser.getText(),tabUsuariosAlterar.getSelectedRow(), 3);
                    tabUsuariosPesquisa.setValueAt(txtAlterarUser.getText(),tabUsuariosAlterar.getSelectedRow(), 3);
                    tabUsuariosAlterar.setValueAt(txtAlterarUser.getText(),tabUsuariosAlterar.getSelectedRow(), 3);
                    txtAlterarUser.setText("");
                }else{
                    JOptionPane.showMessageDialog(null, "O usuário já está sendo utilizado!", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            }
        }else{
           JOptionPane.showMessageDialog(null, "Selecione um usuário para editar.");
       }
    }//GEN-LAST:event_botaoAlterarUserActionPerformed

