    private void botaoAlterarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAlterarProdutoActionPerformed
        if(tabProdutosAlterar.getSelectedRow() != -1){ 
            if(boxAlterarProduto.getSelectedItem()=="Descrição"){
                tabProdutosAlterar.setValueAt(txtAlteracao.getText(),tabProdutosAlterar.getSelectedRow(), 0);
                tabProdutos.setValueAt(txtAlteracao.getText(),tabProdutosAlterar.getSelectedRow(), 0);
                tabProdutos1.setValueAt(txtAlteracao.getText(),tabProdutosAlterar.getSelectedRow(), 0);
                tabProdutosPesquisar.setValueAt(txtAlteracao.getText(),tabProdutosAlterar.getSelectedRow(), 0);
                BancoDeDados.editarProdutoDescricao(tabProdutosAlterar.getValueAt(tabProdutosAlterar.getSelectedRow(), 3).toString(), txtAlteracao.getText());
                if(tabProdutosAlterar.getSelectedRow() != -1){
                    produtoSelecionado.setText(tabProdutosAlterar.getValueAt(tabProdutosAlterar.getSelectedRow(),0).toString());
                }else{
                    produtoSelecionado.setText("");
                }
            }
            if(boxAlterarProduto.getSelectedItem()=="Quantidade"){
                boolean erro=false;
                int quantidade=0;
                try{
                    quantidade = Integer.parseInt(txtAlteracao.getText());
                }catch(NumberFormatException ex){
                    erro = true;
                    JOptionPane.showMessageDialog(null, "Preço Inválido", "Erro", JOptionPane.ERROR_MESSAGE);
                }
                if(erro==false && quantidade>0){
                    tabProdutosAlterar.setValueAt(txtAlteracao.getText(),tabProdutosAlterar.getSelectedRow(), 1);
                    tabProdutos.setValueAt(txtAlteracao.getText(),tabProdutosAlterar.getSelectedRow(), 1);
                    tabProdutos1.setValueAt(txtAlteracao.getText(),tabProdutosAlterar.getSelectedRow(), 1);
                    tabProdutosPesquisar.setValueAt(txtAlteracao.getText(),tabProdutosAlterar.getSelectedRow(), 1);
                    BancoDeDados.editarProdutoQuantidade(tabProdutosAlterar.getValueAt(tabProdutosAlterar.getSelectedRow(), 3).toString(), quantidade);
                }
            }
            if(boxAlterarProduto.getSelectedItem()=="Preço"){
                boolean erro=false;
                double preco=0;
                try{
                    preco = Double.parseDouble(txtAlteracao.getText());
                }catch(NumberFormatException ex){
                    erro = true;
                    JOptionPane.showMessageDialog(null, "Preço Inválido", "Erro", JOptionPane.ERROR_MESSAGE);
                }
                if(erro==false && preco>0){
                    tabProdutosAlterar.setValueAt(txtAlteracao.getText(),tabProdutosAlterar.getSelectedRow(), 2);
                    tabProdutos.setValueAt(txtAlteracao.getText(),tabProdutosAlterar.getSelectedRow(), 2);
                    tabProdutos1.setValueAt(txtAlteracao.getText(),tabProdutosAlterar.getSelectedRow(), 2);
                    tabProdutosPesquisar.setValueAt(txtAlteracao.getText(),tabProdutosAlterar.getSelectedRow(), 2);
                    BancoDeDados.editarProdutoPreco(tabProdutosAlterar.getValueAt(tabProdutosAlterar.getSelectedRow(), 3).toString(), preco);
                }
            }
        }else{
           JOptionPane.showMessageDialog(null, "Selecione um produto para editar.");
       }
    }//GEN-LAST:event_botaoAlterarProdutoActionPerformed

