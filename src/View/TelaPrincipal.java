    private void botaoAlterarUser1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAlterarUser1ActionPerformed
        if(tabClientesAlterar.getSelectedRow() != -1){ 
            if(comboBoxAlterarCliente.getSelectedItem()=="Nome"){
                BancoDeDados.editarClienteNome(tabClientesAlterar.getValueAt(tabClientesAlterar.getSelectedRow(), 0).toString(), txtAlterarCliente.getText());
                tabExcluirClientes.setValueAt(txtAlterarCliente.getText(), tabClientesAlterar.getSelectedRow(), 1);
                tabPesquisaClientes.setValueAt(txtAlterarCliente.getText(),tabClientesAlterar.getSelectedRow(), 1);
                tabClientesAlterar.setValueAt(txtAlterarCliente.getText(),tabClientesAlterar.getSelectedRow(), 1);
                txtAlterarCliente.setText("");
            }
            if(comboBoxAlterarCliente.getSelectedItem()=="CPF"){
                String testeCpf = txtAlterarCliente.getText();
                if(testeCpf.length() == 11){
                    testeCpf = testeCpf.substring(0,3)+"."+
                               testeCpf.substring(3,6)+"."+
                               testeCpf.substring(6,9)+"-"+
                               testeCpf.substring(9,11);
                    String protegeCpf = testeCpf;
                    protegeCpf = protegeCpf.replace(protegeCpf.substring(3, 10), ".***.**");
                    BancoDeDados.editarClienteCpf(tabClientesAlterar.getValueAt(tabClientesAlterar.getSelectedRow(), 0).toString(), txtAlterarCliente.getText());
                    tabExcluirClientes.setValueAt(protegeCpf,tabClientesAlterar.getSelectedRow(), 2);
                    tabPesquisaClientes.setValueAt(protegeCpf,tabClientesAlterar.getSelectedRow(), 2);
                    tabClientesAlterar.setValueAt(protegeCpf,tabClientesAlterar.getSelectedRow(), 2);
                    txtAlterarCliente.setText("");
                }else{
                    JOptionPane.showMessageDialog(null,"Número de CPF inválido! (11 digitos)");
                }
            }
            if(comboBoxAlterarCliente.getSelectedItem()=="Cidade"){
                BancoDeDados.editarClienteCidade(tabClientesAlterar.getValueAt(tabClientesAlterar.getSelectedRow(), 0).toString(), txtAlterarCliente.getText());
                tabExcluirClientes.setValueAt(txtAlterarCliente.getText(), tabClientesAlterar.getSelectedRow(), 3);
                tabPesquisaClientes.setValueAt(txtAlterarCliente.getText(),tabClientesAlterar.getSelectedRow(), 3);
                tabClientesAlterar.setValueAt(txtAlterarCliente.getText(),tabClientesAlterar.getSelectedRow(), 3);
                txtAlterarCliente.setText("");
            }
            if(comboBoxAlterarCliente.getSelectedItem()=="UF"){
                BancoDeDados.editarClienteUF(tabClientesAlterar.getValueAt(tabClientesAlterar.getSelectedRow(), 0).toString(), txtAlterarCliente.getText());
                tabExcluirClientes.setValueAt(txtAlterarCliente.getText(), tabClientesAlterar.getSelectedRow(), 4);
                tabPesquisaClientes.setValueAt(txtAlterarCliente.getText(),tabClientesAlterar.getSelectedRow(), 4);
                tabClientesAlterar.setValueAt(txtAlterarCliente.getText(),tabClientesAlterar.getSelectedRow(), 4);
                txtAlterarCliente.setText("");
            }
             if(comboBoxAlterarCliente.getSelectedItem()=="Telefone"){
                String testeTelefone = txtAlterarCliente.getText();
                if(testeTelefone.length() == 11){
                    String editaTelefone = testeTelefone;
                    editaTelefone = "("+editaTelefone.substring(0,2)+")"+editaTelefone.substring(2,11);
                    BancoDeDados.editarClienteTelefone(tabClientesAlterar.getValueAt(tabClientesAlterar.getSelectedRow(), 0).toString(), txtAlterarCliente.getText());
                    tabExcluirClientes.setValueAt(editaTelefone, tabClientesAlterar.getSelectedRow(), 5);
                    tabPesquisaClientes.setValueAt(editaTelefone,tabClientesAlterar.getSelectedRow(), 5);
                    tabClientesAlterar.setValueAt(editaTelefone,tabClientesAlterar.getSelectedRow(), 5);
                    txtAlterarCliente.setText("");
                }else{
                    JOptionPane.showMessageDialog(null,"Número de telefone inválido!");
                }
            }
        }else{
           JOptionPane.showMessageDialog(null, "Selecione um usuário para editar.");
       }
    }//GEN-LAST:event_botaoAlterarUser1ActionPerformed
