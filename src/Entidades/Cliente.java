
package Entidades;

public class Cliente extends Pessoa {
    private String dataDeNascimento,sexo,cidade,uf,endereco,numero,telefone;

    public String getDataDeNascimento() {
        return dataDeNascimento;
    }

    public void setDataDeNascimento(String dataDeNascimento) {
        this.dataDeNascimento = dataDeNascimento;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    public String getCidade() {
        return cidade;
    }

    public void setCidade(String cidade) {
        this.cidade = cidade;
    }

    public String getUf() {
        return uf;
    }

    public void setUf(String uf) {
        this.uf = uf;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    @Override
    public String toString() {
        return "Cliente{"+"ID:"+ getId() + ", Nome:"+ getNome() + ", dataDeNascimento=" + dataDeNascimento + ", sexo=" + sexo + ", cidade=" + cidade + ", uf=" + uf + ", endereco=" + endereco + ", numero=" + numero + ", telefone=" + telefone + '}';
    }
    

    
}